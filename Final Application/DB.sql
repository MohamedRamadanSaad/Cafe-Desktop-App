CREATE OR REPLACE FORCE VIEW MENULIST
(
   PRODUCT_ID,
   PRODUCTGROUP_ID,
   PRODUCT,
   PGROUP,
   PRICE
)
AS
   SELECT P.PRODUCT_ID,
          PG.PRODUCTGROUP_ID,
          P.NAME product,
          pg.name pgroup,
          P.PRICE
     FROM PRODUCT p
          LEFT JOIN PRODUCTGROUP pg ON P.PRODUCTGROUP_ID = PG.PRODUCTGROUP_ID;


